    protected void datatable(){
 Object[] Baris ={"ID Tunjangan", "T. Kesra","T. Sehat", "T. Transportasi", "T. Beras"};
 tabmode = new DefaultTableModel(null, Baris);
 String find=cari.getText();
 try {
 String sql = "SELECT * FROM tunjangan where id_tunjangan like '%"+find+"%' order by id_tunjangan asc";
 java.sql.Statement stat = conn.createStatement();
 ResultSet hasil = stat.executeQuery(sql);
 while (hasil.next()){
 tabmode.addRow(new Object[]{
 hasil.getString(1),
 hasil.getString(2),
 hasil.getString(3),
 hasil.getString(4),
 hasil.getString(5)
 });
 }
 tabletunjangan.setModel(tabmode);
 } catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Error table tunjangan: "+e);
 }
}

