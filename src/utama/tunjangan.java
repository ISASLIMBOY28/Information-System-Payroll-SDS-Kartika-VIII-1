    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // edit button
        try{
            String sql = "update tunjangan set kesra=?,sehat=?,transport=?,beras=? where id_tunjangan='"+idtunjangan.getText()+"'";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kesra.getText());
            stat.setString(2, sehat.getText());
            stat.setString(3, transport.getText());
            stat.setString(4, beras.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Edit tunjangan berhasil!");
            kosong();
            idtunjangan.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Error edit tunjangan: "+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

